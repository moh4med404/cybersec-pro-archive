# Lambda function: multiply a number by 7
multiply_by_7 = lambda x: x * 7
print(multiply_by_7(3))  # Output: 21

# Zip example: combine two lists into pairs
list1 = ['a', 'b', 'c']
list2 = [1, 2, 3]
combined = zip(list1, list2)
print(list(combined))  # Output: [('a', 1), ('b', 2), ('c', 3)]

# New line example: print names on separate lines
print("Mohamed\nAdam")
# Output:
# Mohamed
# Adam
