"""
Arithmetic Operators in Python:

1. Addition (+)        : Adds two numbers.
2. Subtraction (-)     : Subtracts second number from the first.
3. Multiplication (*)  : Multiplies two numbers.
4. Division (/)        : Divides first number by second (result is float).
5. Floor Division (//) : Divides and returns the integer part of the quotient.
6. Modulus (%)         : Returns the remainder of the division.
7. Exponentiation (**) : Raises first number to the power of the second.

These operators work with numeric types like int and float.
"""

# Demonstration of Arithmetic Operators in Python

# Two numbers for demonstration
a = 15
b = 4

# Addition
addition = a + b
print(f"{a} + {b} = {addition}")

# Subtraction
subtraction = a - b
print(f"{a} - {b} = {subtraction}")

# Multiplication
multiplication = a * b
print(f"{a} * {b} = {multiplication}")

# Division (float result)
division = a / b
print(f"{a} / {b} = {division}")

# Floor Division (integer division)
floor_division = a // b
print(f"{a} // {b} = {floor_division}")

# Modulus (remainder)
modulus = a % b
print(f"{a} % {b} = {modulus}")

# Exponentiation (power)
exponentiation = a ** b
print(f"{a} ** {b} = {exponentiation}")
